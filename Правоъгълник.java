public class Правоъгълник {


    public static void main(String[] args) {
    Perimeter();
    Area();


    }
    public static void Perimeter() {
        int a = 2;
        int b = 6;
        int P = 2*a + 2*b;
        System.out.println("Периметъра на правоъгълника е : " + P);


    }
    public static void Area() {
        int a = 2;
        int b = 6;
        int S = a*b;
        System.out.println("Лицето на Правоъгълника е :"+ S);
    }
}
